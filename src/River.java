public class River
{
    public static int RiverBaseX;
    public static int RiverBaseY;

    public static int getRiverBaseX()
    {
        return RiverBaseX;
    }

    public static int getRiverBaseY()
    {
        return RiverBaseY;
    }

    public River(int x, int y)
    {
        RiverBaseX = x;
        RiverBaseY = y;
    }


}
